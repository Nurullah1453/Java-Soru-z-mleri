package sorular5;

public class Soru1 {

    // 1-4 arasindaki sayilar icin carpim tablosu olusturalim

    // dis dongu satirlari kontrol eder

    public static void main(String[] args) {

        for (int i = 1; i <=4 ; i++) {

            for (int j = 1; j <=4 ; j++) {

                System.out.print(i*j+" ");

            }
            System.out.println("");

        }

    }
}
